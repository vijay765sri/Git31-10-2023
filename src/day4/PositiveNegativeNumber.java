package day4;

public class PositiveNegativeNumber {
	
	public static void main(String[]args)
	{
		int num = -10;
		
		if(num>0)
		{
			System.out.println("Number is Positive");
		}
		else if(num<0)
		{
			System.out.println("Number is Negative");
		}
		else
		{
			System.out.println("Number is Zero");
		}
	}

}
